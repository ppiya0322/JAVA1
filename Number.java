
public class Number {

	public static void main(String[] args) {
		// 연산자(Operator): + - * / 등등
		System.out.println(6+2); // 더하기, 8
		System.out.println(6-2); // 빼기, 4
		System.out.println(6*2); // 곱하기, 12
		System.out.println(6/2); // 나누기, 3
		
		System.out.println(Math.PI); // 원주율, 3.14.....
		//수학과 관련된 명령! 캐비닛 같은 것....=(Math.~);
		System.out.println(Math.floor(Math.PI)); // 3, 소수점 내리기
		System.out.println(Math.ceil(Math.PI)); // 4, 소수점 올리기
	}

}
